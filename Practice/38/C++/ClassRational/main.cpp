class Rational {
    int num, denom, sign;
    int whatSign(int a) {
        if (a < 0) {
            return -1;
        }
        else {
            return 1;
        }
    }

public:
    static int gcd(int a, int b) {
        if (a == 0) {
            return b;
        }
        if (b == 0) {
            return a;
        }
        if (b > a) {
            std::swap(a, b);
        }
        for (int i = b; i > 1; --i) {
            if (a % i == 0 and b % i == 0) {
                return i;
            }
        }
        return 1;
    }
    Rational() : num(0), denom(1), sign(1) {}

    Rational(int a, int b): num(abs(a)), denom(abs(b)), sign(whatSign(a) == whatSign(b) ? 1 : -1) {
        if (a != 0 and b != 0) {
            int gcd = Rational::gcd(num, denom);
            num /= gcd;
            denom /= gcd;
        }
    }

    bool operator ==(const Rational& r) const {
        if (isNaN() or r.isNaN()) {
            return false;
        }
        if (num == 0 and r.num == 0) {
            return true;
        }
        if (sign != r.sign) {
            return false;
        }
        if (denom == 0 and r.denom == 0) {
            return true;
        }
        return num == r.num and denom == r.denom;
    }

    Rational operator -(const Rational& r) const {
        Rational rCopy = r;
        rCopy.sign = -rCopy.sign;

        return *this + rCopy;
    }

    Rational operator +(const Rational& r) const {
        if (isNaN() or r.isNaN()) {
            return Rational(0, 0);
        }
        if (denom == 0 and r.denom == 0) {
            if (sign == r.sign) {
                return r;
            }
            else {
                return Rational(0, 0);
            }
        }
        return Rational((sign * num * r.denom) + (r.sign * r.num * denom), denom * r.denom);
    }

    Rational operator *(const Rational& r) const {
        Rational res(num * r.num, denom * r.denom);
        res.sign = sign * r.sign;
        return res;
    }

    Rational operator /(const Rational& r) const {
        auto res = Rational(num * r.denom, denom * r.numerator());

        res.sign = sign * r.sign;
        return res;
    }

    operator double() const {
        return sign * num / double(denom);
    }

    operator bool() const {
        return num != 0 or denom == 0;
    }

    int numerator() const {
        return num;
    }

    int denominator() const {
        return denom;
    }

    bool isNaN() const {
        return num == 0 and denom == 0;
    }
};


